class ReportScreen extends StatefulWidget {
  const ReportScreen({super.key});

  @override
  State<ReportScreen> createState() => _ReportScreenState();
}

class _ReportScreenState extends State<ReportScreen>
    with SingleTickerProviderStateMixin {
  late TabController _tabController;

  // Overview
  DateTime _overviewDate = DateTime.now();

  // Sales Report
  DateTime _salesStart = DateTime.now().subtract(const Duration(days: 30));
  DateTime _salesEnd = DateTime.now();

  // Customer Purchases
  DateTime _custStart = DateTime.now().subtract(const Duration(days: 30));
  DateTime _custEnd = DateTime.now();
  int? _selectedCustomerId;

  // Receipts
  final _receiptSearchCtrl = TextEditingController();
  DateTime? _receiptStart;
  DateTime? _receiptEnd;

  // Product History
  DateTime _prodStart = DateTime.now().subtract(const Duration(days: 30));
  DateTime _prodEnd = DateTime.now();
  int? _selectedProductId;

  // Expenses
  DateTime _expStart = DateTime.now().subtract(const Duration(days: 30));
  DateTime _expEnd = DateTime.now();

  @override
  void initState() {
    super.initState();
    _tabController = TabController(length: 8, vsync: this);
    _tabController.addListener(() {
      if (!_tabController.indexIsChanging) _loadTab(_tabController.index);
    });
    _loadTab(0);
  }

  @override
  void dispose() {
    _tabController.dispose();
    _receiptSearchCtrl.dispose();
    super.dispose();
  }

  void _loadTab(int i) {
    final b = context.read<ReportBloc>();
    switch (i) {
      case 0:
        b.add(LoadDailySalesSummary(_overviewDate));
        b.add(LoadInventoryValuation());
        break;
      case 1:
        b.add(LoadSalesReport(_startOfDay(_salesStart), _endOfDay(_salesEnd)));
        break;
      case 2:
        b.add(const LoadCustomerLedger());
        break;
      case 3:
        b.add(LoadCustomerPurchases(
          _startOfDay(_custStart), _endOfDay(_custEnd),
          customerId: _selectedCustomerId,
        ));
        break;
      case 4:
        b.add(FindReceiptsForReprint(
          _receiptSearchCtrl.text,
          start: _receiptStart,
          end: _receiptEnd,
        ));
        break;
      case 5:
        b.add(LoadProductSalesHistory(
          _startOfDay(_prodStart), _endOfDay(_prodEnd),
          productId: _selectedProductId,
        ));
        break;
      case 6:
        b.add(const LoadInventoryReport());
        break;
      case 7:
        b.add(LoadExpenseReport(_startOfDay(_expStart), _endOfDay(_expEnd)));
        break;
    }
  }

  Future<pw.MemoryImage?> _loadLogo(String path) async {
    if (path.isEmpty) return null;
    try {
      final f = File(path);
      if (await f.exists()) return pw.MemoryImage(await f.readAsBytes());
    } catch (_) {}
    return null;
  }

  Map<String, String> _settings(BuildContext ctx) {
    final s = ctx.read<SettingsBloc>().state;
    return s is SettingsLoaded ? s.settings : {};
  }

  // ── Date range picker helper ────────────────────────────────────────────
  Future<void> _pickDateRange(
    DateTime start,
    DateTime end,
    void Function(DateTime, DateTime) onPicked,
  ) async {
    final picked = await showDateRangePicker(
      context: context,
      firstDate: DateTime(2020),
      lastDate: DateTime.now().add(const Duration(days: 1)),
      initialDateRange: DateTimeRange(start: start, end: end),
    );
    if (picked != null) onPicked(picked.start, picked.end);
  }

