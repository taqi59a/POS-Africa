String _fmtDate(DateTime d) =>
    '${d.day.toString().padLeft(2, '0')}/${d.month.toString().padLeft(2, '0')}/${d.year}';

String _fmtDateTime(DateTime d) =>
    '${_fmtDate(d)}  ${d.hour.toString().padLeft(2, '0')}:${d.minute.toString().padLeft(2, '0')}';

DateTime _startOfDay(DateTime d) => DateTime(d.year, d.month, d.day);
DateTime _endOfDay(DateTime d) => DateTime(d.year, d.month, d.day, 23, 59, 59);

pw.Widget _pdfHeader(String businessName, String businessAddress,
    String businessPhone, pw.MemoryImage? logo) {
  return pw.Column(crossAxisAlignment: pw.CrossAxisAlignment.center, children: [
    if (logo != null) ...[
      pw.Center(child: pw.Image(logo, height: 50, fit: pw.BoxFit.contain)),
      pw.SizedBox(height: 6),
    ],
    pw.Text(businessName,
        style: pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold),
        textAlign: pw.TextAlign.center),
    if (businessAddress.isNotEmpty)
      pw.Text(businessAddress,
          style: const pw.TextStyle(fontSize: 10), textAlign: pw.TextAlign.center),
    if (businessPhone.isNotEmpty)
      pw.Text(businessPhone,
          style: const pw.TextStyle(fontSize: 10), textAlign: pw.TextAlign.center),
    pw.SizedBox(height: 6),
    pw.Divider(thickness: 1),
  ]);
}

pw.Widget _pdfFooter() => pw.Column(children: [
      pw.Divider(thickness: 0.5),
      pw.Center(
        child: pw.Text(
          'Generated by POS Africa  •  ${_fmtDateTime(DateTime.now())}',
          style: const pw.TextStyle(fontSize: 9, color: PdfColors.grey600),
        ),
      ),
    ]);

pw.TableRow _pdfHeaderRow(List<String> cells) => pw.TableRow(
      decoration: const pw.BoxDecoration(color: PdfColors.blueGrey700),
      children: cells
          .map((c) => pw.Padding(
                padding: const pw.EdgeInsets.symmetric(horizontal: 6, vertical: 5),
                child: pw.Text(c,
                    style: pw.TextStyle(
                        fontSize: 9,
                        fontWeight: pw.FontWeight.bold,
                        color: PdfColors.white)),
              ))
          .toList(),
    );

pw.TableRow _pdfDataRow(List<String> cells, {bool isEven = false}) =>
    pw.TableRow(
      decoration: pw.BoxDecoration(
          color: isEven ? PdfColors.blueGrey50 : PdfColors.white),
      children: cells
          .map((c) => pw.Padding(
                padding: const pw.EdgeInsets.symmetric(horizontal: 6, vertical: 4),
                child: pw.Text(c, style: const pw.TextStyle(fontSize: 9)),
              ))
          .toList(),
    );

