  Future<void> _reprintSaleDetail(Map<String, dynamic> detail) async {
    final s = _settings(context);
    final logo = await _loadLogo(s['business_logo_path'] ?? '');
    final businessName = s['business_name'] ?? 'POS Africa';
    final businessAddress = s['business_address'] ?? '';
    final businessPhone = s['business_phone'] ?? '';
    final dualCurrency = (s['dual_currency_enabled'] ?? 'false') == 'true';
    final footer = s['receipt_footer'] ?? 'Merci pour votre achat!';
    final vatEnabled = (s['vat_enabled'] ?? 'false') == 'true';
    final widthMm = double.tryParse(s['receipt_paper_width_mm'] ?? '80') ?? 80;
    final vatPct = double.tryParse(s['vat_percentage'] ?? '16') ?? 16;
    final vatNumber = s['business_vat_number'] ?? '';

    final doc = pw.Document();

    pw.TextStyle bold(double sz) =>
        pw.TextStyle(fontSize: sz, fontWeight: pw.FontWeight.bold);
    pw.TextStyle norm(double sz) => pw.TextStyle(fontSize: sz);

    final pageWidth = widthMm * PdfPageFormat.mm;
    final margin = (widthMm >= 100 ? 8.0 : 4.0) * PdfPageFormat.mm;

    final txn = detail['transactionNumber']?.toString() ?? '';
    final saleDate = detail['saleDate'] as DateTime? ?? DateTime.now();
    final lines = (detail['lines'] as List?)?.cast<Map<String, dynamic>>() ?? [];
    final subtotal = (detail['subtotal'] as double? ?? 0);
    final discount = (detail['discountAmount'] as double? ?? 0);
    final vatAmount = (detail['vatAmount'] as double? ?? 0);
    final grandTotal = (detail['grandTotal'] as double? ?? 0);
    final grandTotalUsd = (detail['grandTotalUsd'] as double? ?? 0);
