  Future<void> _exportDailyReportPdf({bool salesOnly = false, bool stockOnly = false}) async {
    final s = _settings(context);
    final logo = await _loadLogo(s['business_logo_path'] ?? '');
    final state = context.read<ReportBloc>().state;
    if (state is! ReportDataState) return;

    final doc = pw.Document();
    final date = _fmtDate(_overviewDate);

    doc.addPage(pw.Page(
      pageFormat: PdfPageFormat.a4,
      margin: const pw.EdgeInsets.all(28),
      build: (ctx) => pw.Column(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          _pdfHeader(s['business_name'] ?? 'POS Africa',
              s['business_address'] ?? '', s['business_phone'] ?? '', logo),
          pw.Center(
            child: pw.Text('Daily Report  —  $date',
                style: pw.TextStyle(fontSize: 15, fontWeight: pw.FontWeight.bold)),
          ),
          pw.SizedBox(height: 14),
          if (!stockOnly) ...[
            pw.Text('Sales Summary',
                style: pw.TextStyle(fontSize: 13, fontWeight: pw.FontWeight.bold,
                    color: PdfColors.blueGrey800)),
            pw.SizedBox(height: 6),
            if (state.dailySummary != null) ...[
              _buildSummaryTable(state.dailySummary!),
            ] else
              pw.Text('No sales data.', style: const pw.TextStyle(fontSize: 10)),
            pw.SizedBox(height: 18),
          ],
          if (!salesOnly) ...[
            pw.Text('Stock / Inventory Status',
                style: pw.TextStyle(fontSize: 13, fontWeight: pw.FontWeight.bold,
                    color: PdfColors.blueGrey800)),
            pw.SizedBox(height: 6),
            if (state.inventoryReport != null && state.inventoryReport!.isNotEmpty)
              _buildInventoryTable(state.inventoryReport!)
            else if (state.inventoryValuation != null)
              pw.Text(
                  'Total Inventory Value: CDF ${state.inventoryValuation!.toStringAsFixed(0)}',
                  style: const pw.TextStyle(fontSize: 10))
            else
              pw.Text('Load inventory tab first.', style: const pw.TextStyle(fontSize: 10)),
          ],
          pw.Spacer(),
          _pdfFooter(),
        ],
      ),
    ));

    await Printing.layoutPdf(
      onLayout: (_) => doc.save(),
      name: 'Daily_Report_${date.replaceAll('/', '-')}',
    );
  }

